#Construa um programa que calcule:
#Quanto custa encher o tanque de um carro que tem 50 litros de capacidade, está com com 20 litros de combustível atualmente e o custo do combustível é de €5,80/litro?

Capacidade = 50
Tanque = 20
Valor = 5.80

#Para abastecer o tanque vamos efetuar o calculo de quantos litros faltam para completar * o valor por litro.

Custo = (Capacidade - Tanque) * Valor

print(Custo)
